local function desc(description)
  return { noremap = true, silent = true, desc = description }
end
vim.keymap.set('n', '<space>ss', function()
  vim.cmd.ISwap()
end, desc('i[s]wap: [s]wap parameters'))
vim.keymap.set('n', '<space>sw', function()
  vim.cmd.ISwapWith()
end, desc('iswap: [s]wap parameter [w]ith'))
vim.keymap.set('n', '<space>snn', function()
  vim.cmd.ISwapNode()
end, desc('iswap: [s]wap [nn]odes'))
vim.keymap.set('n', '<space>snw', function()
  vim.cmd.ISwapNodeWith()
end, desc('iswap: [s]wap [n]ode [w]ith'))
vim.keymap.set('n', '<space>smm', function()
  vim.cmd.IMove()
end, desc('i[s]wap: [mm]ove'))
vim.keymap.set('n', '<space>smw', function()
  vim.cmd.IMoveWith()
end, desc('i[s]wap: [m]ove [w]ith'))
